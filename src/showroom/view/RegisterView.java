    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());
        String confirmPassword = new String(txtConfirmPassword.getPassword());
        String fullName = txtFullName.getText();

        // 2. Kiểm tra dữ liệu đầu vào (Validation)
        if (username.isEmpty() || password.isEmpty() || confirmPassword.isEmpty() || fullName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu nhập lại không khớp.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (userDAO.isUsernameExists(username)) {
            JOptionPane.showMessageDialog(this, "Tên đăng nhập đã tồn tại. Vui lòng chọn tên khác.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 3. Tạo đối tượng User và gọi DAO để đăng ký
        User newUser = new User();
        newUser.setUsername(username);
        // Trong thực tế, bạn phải băm (hash) mật khẩu trước khi lưu!
        newUser.setPassword(password);
        newUser.setFullName(fullName);
        newUser.setRole("staff"); // Mặc định vai trò là nhân viên

        boolean success = userDAO.registerUser(newUser);

        // 4. Thông báo kết quả
        if (success) {
            JOptionPane.showMessageDialog(this, "Đăng ký thành công! Vui lòng đăng nhập.", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            this.dispose(); // Đóng cửa sổ đăng ký
        } else {
            JOptionPane.showMessageDialog(this, "Đăng ký thất bại, đã có lỗi xảy ra.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

